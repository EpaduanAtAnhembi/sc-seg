package exemplos;

public class Exemplo03 {
    public static void main(String[] args) {
        // Operadores relacionais
        // > >= < <= == !=
        System.out.println(10 > 5);
        System.out.println(10 < 5);
        System.out.println(10 != 5);
        System.out.println(10 == 10);

        boolean resp = 10 > 4;
        System.out.println(resp);

    }
}
